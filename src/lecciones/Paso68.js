                            // Trabajando con Métodos de Formateo de Cadenas

// ¿Cómo puedes cambiar el caso de una cadena?

// basicamente es la forma de como cambiar el formato de una cadena de texto, por ejemplo, puedes convertir una cadena a mayúsculas o minúsculas utilizando los métodos .toUpperCase() y .toLowerCase().

//.toUpperCase()

let name = "Daniel";
console.log(name.toUpperCase());

let lastName = "Rairan";
let fullName = name + " " + lastName.toUpperCase();
console.log(fullName);

//.toLowerCase()

let name1 = "Daniel";
console.log(name1.toLowerCase());

let lastName1 = "Rairan";
let fullName1 = name1 + " " + lastName1.toLowerCase();
console.log(fullName1);

// En resumen, los métodos toUpperCase() y toLowerCase() en JavaScript son herramientas poderosas para transformar cadenas en todas letras mayúsculas o minúsculas.